    private void btnAceitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceitarActionPerformed
        int linha = tblClientes.getSelectedRow();
        if (linha > -1) {
            codCli = Integer.valueOf(String.valueOf(tblClientes.getValueAt(linha, 0)));
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente!", "Informação", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAceitarActionPerformed

